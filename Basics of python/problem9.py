# take short name of day and print full name of the day

day=input('enter shortcut name of the day:')
if(day=='sun'):
    print('SUNDAY')
elif(day=='mon'):
    print('MONDAY')
elif(day=='tue'):
    print('TUESDAY')
elif(day=='wed'):
    print('WEDNESDAY')
elif(day=='thu'):
    print('THURSDAY')
elif(day=='fri'):
    print('FRIDAY')
elif(day=='sat'):
    print('SATURDAY')