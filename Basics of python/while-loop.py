# while-loop is exicute upto given condtion is true
# if given condition is false then while loop will be stop.
#  { initialization
#   while (condition):
#        statements
#        increment or decrement
#  }

i=3
while(i<10):
    print(i)
    i+=1

r=10
while(r>0):
    print(r)
    r-=1
